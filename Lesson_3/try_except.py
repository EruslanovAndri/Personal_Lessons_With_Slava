
# Конструкция try/ except проверяет код на ошибки и сообщает о них пользователю.

def f(x:int):
    return x ** 2

try:
    num = int(input('-->')) # Проверяем корректность ввода данных 
except ValueError:
    print('Вы ввели не число') # Если данные не корректны выводим ошибку-сообщение
else:
    print(f(num)) # Если данные корректны, применяем функцию f()



